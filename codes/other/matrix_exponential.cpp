
void exp(LL m[2][2], LL x){
    LL c[2][2] = {{1,1},{1,0}},n[2][2];
    n[0][0] = m[0][0]*c[0][0] + m[0][1]*c[1][0];
    n[0][1] = m[0][0]*c[0][1] + m[0][1]*c[1][1];
    n[1][0] = m[1][0]*c[0][0] + m[1][1]*c[1][0];
    n[1][1] = m[1][0]*c[0][1] + m[1][1]*c[1][1];
    if(x != 1)
        exp(n,x-1);
    else
        cout << n[0][0];
}
